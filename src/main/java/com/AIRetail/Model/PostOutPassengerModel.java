package com.AIRetail.Model;

public class PostOutPassengerModel {
	private Integer rankid;
	private String token;
	private String face_pic;
	private String gender;
	private String age;
	private String whole_body_pic;
	private String timestamp;
	public Integer getRankid() {
		return rankid;
	}
	public void setRankid(Integer rankid) {
		this.rankid = rankid;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getFace_pic() {
		return face_pic;
	}
	public void setFace_pic(String face_pic) {
		this.face_pic = face_pic;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getWhole_body_pic() {
		return whole_body_pic;
	}
	public void setWhole_body_pic(String whole_body_pic) {
		this.whole_body_pic = whole_body_pic;
	}
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	
}
